void CheckForWinning(){
	// horizontal check
	if(xoGrid[0][0] == xoGrid[0][1] && xoGrid[0][1]== xoGrid[0][2]  &&xoGrid[0][2] != 0){
		if(xoGrid[0][2] == 1) {Xscore++ ;} else{ Yscore++ ;} SetMatrix();
		
	}else if (xoGrid[1][0] == xoGrid[1][1] && xoGrid[1][1]== xoGrid[1][2] &&xoGrid[1][2] != 0){
		if(xoGrid[1][2] == 1) {Xscore++ ;} else{ Yscore++ ;} SetMatrix();
		
	}else if (xoGrid[2][0] == xoGrid[2][1] && xoGrid[2][1]== xoGrid[2][2] &&xoGrid[2][2] != 0) {
		if(xoGrid[2][2] == 1) {Xscore++ ;} else{ Yscore++ ;} SetMatrix();
	} else  // vertical check 
		if(xoGrid[0][0] == xoGrid[1][0] && xoGrid[1][0]== xoGrid[2][0]  &&xoGrid[2][0] != 0){
			if(xoGrid[2][0] == 1) {Xscore++ ;} else{ Yscore++ ;} SetMatrix();
		
	}else if (xoGrid[0][1] == xoGrid[1][1] && xoGrid[1][1]== xoGrid[2][1] &&xoGrid[2][1] != 0){
		if(xoGrid[2][1] == 1) {Xscore++ ;} else{ Yscore++ ;}  SetMatrix();
		
	}else if (xoGrid[0][2] == xoGrid[1][2] && xoGrid[1][2]== xoGrid[2][2] &&xoGrid[2][2] != 0) {
		if(xoGrid[2][2] == 1) {Xscore++ ;} else{ Yscore++ ;} SetMatrix();
	}else // cross check
		if(xoGrid[0][0] == xoGrid[1][1] && xoGrid[1][1]== xoGrid[2][2] &&xoGrid[2][2] != 0 ){
			if(xoGrid[2][2] == 1){Xscore++ ;} else{ Yscore++ ;} SetMatrix();
	}else if (xoGrid[0][2] == xoGrid[1][1] && xoGrid[1][1]== xoGrid[2][0] &&xoGrid[2][0] != 0){
		if(xoGrid[1][1] == 1) {Xscore++ ;} else{ Yscore++ ;}  SetMatrix();
	}
}


